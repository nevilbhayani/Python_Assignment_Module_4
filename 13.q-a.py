# Explain Exception handling? What is an Error in Python?

# Syntax Errors:
# Syntax errors occur when the Python interpreter encounters code that violates the language's syntax rules. These errors prevent the program from running.
# Example: Missing closing parenthesis.

# Runtime Errors (Exceptions):
# Runtime errors, or exceptions, occur during the execution of a program. They are not syntax errors but issues that arise while the program is running. Examples include division by zero or accessing an index that doesn't exist.
# Example: Division by zero.

# Logical Errors (Semantic Errors):
# Logical errors do not cause the program to terminate or raise an exception. Instead, they lead to incorrect behavior or output due to logical mistakes in the code.
# Example: Incorrect mathematical operation.